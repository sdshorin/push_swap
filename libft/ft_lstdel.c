#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void*, size_t))
{
	t_list	*next;
	t_list	*now;

	if (!alst || !del)
		return ;
	now = *alst;
	while (now)
	{
		next = now->next;
		ft_lstdelone(&now, (*del));
		now = next;
	}
	*alst = NULL;
}
