#include <string.h>
#include <stdlib.h>
#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*ans;

	ans = malloc(size);
	if (!ans)
		return (NULL);
	ft_memset(ans, 0, size);
	return (ans);
}
